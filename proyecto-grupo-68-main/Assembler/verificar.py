def comparar_textos(texto_correcto, texto_prueba):
    # Dividir los textos en líneas
    lineas_correcto = texto_correcto.splitlines()
    lineas_prueba = texto_prueba.splitlines()
    
    # Inicializar variable de bandera
    textos_iguales = True

    # Obtener la longitud máxima para recorrer ambas listas
    max_lineas = max(len(lineas_correcto), len(lineas_prueba))
    
    for i in range(max_lineas):
        # Obtener las líneas, si están dentro del índice
        linea_correcta = lineas_correcto[i] if i < len(lineas_correcto) else ""
        linea_prueba = lineas_prueba[i] if i < len(lineas_prueba) else ""
        
        # Comparar líneas y mostrar las diferentes
        if linea_correcta != linea_prueba:
            print(f"Diferencia en la línea {i + 1}:")
            print(f"Texto correcto: {linea_correcta}")
            print(f"Texto prueba  : {linea_prueba}\n")
            textos_iguales = False

    print(textos_iguales)

